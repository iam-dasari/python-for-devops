x = str("Hello World") #string
print("String:: ",x)
x = int(20) #integer
print("Integer:: ",x)
x = float(20.5) #float
print("Float:: ",x)
x = complex(1j) #complex
print("Complex:: ",x)
x = list(("apple", "banana", "cherry")) #list
print("List:: ",x)
x = tuple(("apple", "banana", "cherry")) #tuple
print("Tuple:: ",x)
x = range(6) #range
print("Range:: ",x)
x = dict(name="John", age=36) #dictionary
print("Dictionary:: ",x)
x = set(("apple", "banana", "cherry")) #set
print("Set:: ",x)
x = frozenset(("apple", "banana", "cherry")) #frozenset
print("Frozenset:: ",x)
x = bool(5) #boolean
print("Boolean:: ",x)
x = bytes(5) #bytes
print("Bytes:: ",x)
x = bytearray(5) #bytearray
print("Bytearray:: ",x)
